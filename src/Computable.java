/**
 * Interface Computable defines a method for calculating a result based on two String variables.
 */
public interface Computable {

    /**
     * Calculates a result of two String variables.
     * @param number1 First input value as a string.
     * @param number2 Second input value as a string.
     * @return The calculated result of the input as a double.
     */
    double calculate(String number1, String number2);

}
